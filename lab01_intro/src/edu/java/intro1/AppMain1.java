package edu.java.intro1; //패키지 선언문

//클래스 선언(정의)
public class AppMain1 {

	//main 메서드(정의) : 자바 프로그램의 시작과 끝.
	public static void main(String[] args) {
		System.out.println("안녕하세요. 여러분!"); // 모든 문장이 끝날때 세미콜론(;)을 꼭 써줘야 한다.
		
		System.out.println("Hello, 자바!");
	} // end main 
	

} // end class



